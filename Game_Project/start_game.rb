def update
  case
  when Gosu.button_down?(Gosu::KB_LEFT) || Gosu.button_down?(Gosu::GP_LEFT)
    move_player(-4, 0)
  when Gosu.button_down?(Gosu::KB_RIGHT) || Gosu.button_down?(Gosu::GP_RIGHT)
    move_player(4, 0)
  when Gosu.button_down?(Gosu::KB_UP) || Gosu.button_down?(Gosu::GP_BUTTON_0)
    move_player(0, -4)
  when Gosu.button_down?(Gosu::KB_DOWN) || Gosu.button_down?(Gosu::GP_BUTTON_1)
    move_player(0, 4)
  end
  # Move the computer towards the player
    if @computer_x < @player_x
      @computer_x += @computer_speed
    elsif @computer_x > @player_x
      @computer_x -= @computer_speed
    end
    if @computer_y < @player_y
      @computer_y += @computer_speed
    elsif @computer_y > @player_y
      @computer_y -= @computer_speed
    end
end

