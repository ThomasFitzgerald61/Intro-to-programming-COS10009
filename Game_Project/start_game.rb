def move_player(dx, dy)
    @player_x += dx
    @player_y += dy
    if @collision_map && @collision_map[@player_y.floor/32][@player_x.floor/32] == 1
        @player_x -= dx
        @player_y -= dy
    end
    # Move the computer player towards the player's current position
    @computer_x += (@player_x - @computer_x) * 0.01
    @computer_y += (@player_y - @computer_y) * 0.01
    # Check the computer player's position against the collision map
    if @collision_map && @collision_map[@computer_y.floor/32][@computer_x.floor/32] == 1
        @computer_x -= (@player_x - @computer_x) * 0.01
        @computer_y -= (@player_y - @computer_y) * 0.01
    end
    # Check for screen boundaries
    @player_x = [0, @player_x].max
    @player_x = [self.width - @player_image.width * 0.1, @player_x].min
    @player_y = [0, @player_y].max
    @player_y = [self.height - @player_image.height * 0.1, @player_y].min
end

  def draw
      clip_to(0, 0, width, height) do
    @background_image.draw(0, 0, 0, 1, 1)
    @player_image.draw(@player_x, @player_y, 1, 0.1, 0.1)
    @computer_player_image.draw(@computer_x, @computer_y, 1, 0.1, 0.1)
    end
    # Draw the health bar
    draw_quad(0, 0, Gosu::Color::RED, width, 0, Gosu::Color::RED, 0, 10, Gosu::Color::GREEN, width * (@player_health / 100.0), 10, Gosu::Color::GREEN, 1)
  end
end

